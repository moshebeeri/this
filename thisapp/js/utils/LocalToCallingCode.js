async function getCallingCode() {
    try {
        let countryCode = await CarrierInfo.isoCountryCode();
        if (!countryCode) {
            return '972';
        }
        let callingCode = countryCodeToCallingCode[countryCode.toUpperCase()];
        if (!callingCode) {
            return '972';
        }
        return callingCode;
    } catch (err) {
        console.log('mobile not supported ')
        return '972';
    }
}

async function getCurrencySymbol() {
    try {
        let countryCode = await CarrierInfo.isoCountryCode();
        let currency = countryCodeToCurrency[countryCode.toUpperCase()];
        let symbol = getSymbolFromCurrency(currency);
        return symbol;
    } catch (error) {
        return ILS;
    }
}

